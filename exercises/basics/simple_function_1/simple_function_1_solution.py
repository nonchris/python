# keyword def is used to define a function
# say_hello is the name of the function
# () are used to pass arguments to the function (this function has none)
# : is used to start the function body, and simply means "do the following"
# indentation is used to mark the function body
def say_hello():
    print("Hello")
    print("Hello")


say_hello()
say_hello()


def say_name(name):
    print("Hello " + name)
    print("Hello " + name)


# names are arbitrary
say_name("Chris")
say_name("Nik")
