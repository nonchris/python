number = "41"

# convert the string to an integer
number = int(number)

# let's print it and ensure it's type
print(number)
print(type(number))

# add 1 to the integer
number = number + 1

# let's check the value
print(number)

# convert number back to string
number = str(number)

print(number)
print(type(number))

# add a zero to the string
number = number + "0"
print(number)
print(type(number))

# double the string by adding it to itself
number = number + number
print(number)
print(type(number))

# note that the type of the variable 'number' is changed from str to int and back
