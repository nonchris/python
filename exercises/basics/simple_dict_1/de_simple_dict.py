# ein dict (kurz für dictionary) ist eine Datenstruktur, die Schlüssel-Wert-Paare speichert
# wir geben also einem Wert einen Namen (den Schlüssel) und können auf den Wert über seinen Namen zugreifen
# Beispiel:
mein_dict = {
    "schluessel1": "wert1",
    "schluessel2": "wert2"
}

# das ganze dict ausgeben
print(mein_dict)

# den Wert von 'schluessel1' extrahieren
wert = mein_dict["schluessel1"]
print(wert)

# wir können Werte zu einem dict hinzufügen, indem wir den Namen des Schlüssels in eckige Klammern schreiben
# und ihm mit dem = Operator einen Wert zuweisen
mein_dict["schluessel3"] = "wert3"

# schauen wir uns das neue dict an
print(mein_dict)

# wir können auch den Wert eines Schlüssels ändern
mein_dict["schluessel3"] = "neuer Wert"

# jetzt schauen wir, ob der Wert geändert wurde
wert3 = mein_dict["schluessel3"]
print(wert3)

